    private void copyFileOrDirectory(Path source, Path destination) throws IOException {
        if (Files.isDirectory(source)) {
            copyDirectory(source, destination.resolve(source.getFileName()));
        } else {
            copyFile(source, destination.resolve(source.getFileName()));
        }
    }

    private void copyDirectory(Path source, Path destination) throws IOException {
        if (!Files.exists(destination)) {
            Files.createDirectory(destination);
        }
        DirectoryStream<Path> directoryStream = Files.newDirectoryStream(source);
        for (Path path : directoryStream) {
            copyFileOrDirectory(path, destination.resolve(path.getFileName()));
        }
    }

    private void copyFile(Path source, Path destination) throws IOException {
        Files.copy(source, destination, StandardCopyOption.REPLACE_EXISTING);
    }
